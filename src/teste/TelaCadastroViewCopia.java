    private void tableCadastroKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tableCadastroKeyReleased
        // TODO add your handling code here:
        String id = (String) tableCadastro.getValueAt(tableCadastro.getSelectedRow(), 0);
        System.out.println(id);
        con.conectar();
        con.executeSql("SELECT * FROM cadastro WHERE id="+id);
        try{
            con.rs.first();
            mod.setId(con.rs.getInt("id"));

            txtNome.setText(String.valueOf(con.rs.getString("nome")));
            txtTelefone.setText(String.valueOf(con.rs.getString("telefone")));
            txtCidade_origem.setText(String.valueOf(con.rs.getString("cidade_origem")));
            txtCidade_destino.setText(String.valueOf(con.rs.getString("cidade_destino")));
            txtData.setText(String.valueOf(con.rs.getString("data_viagem")));

        } catch(SQLException exception) {
            System.out.println(exception.getMessage());
            JOptionPane.showMessageDialog(null, "Erro ao selecionar");
        }

        con.desconectarBanco();

        txtNome.setEnabled(false);
        txtTelefone.setEnabled(false);
        txtCidade_origem.setEnabled(false);
        txtCidade_destino.setEnabled(false);
        txtData.setEnabled(false);
        btEditar.setEnabled(true);
        btExcluir.setEnabled(true);
    }//GEN-LAST:event_tableCadastroKeyReleased

