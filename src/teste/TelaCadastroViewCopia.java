    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"id","nome","telefone","cidade_origem","cidade_destino","data_viagem"};
        con.conectar();
        con.executeSql(Sql);
        try{
            con.rs.first();
            do{
                dados.add(new Object[]{con.rs.getString("id"),con.rs.getString("nome"),con.rs.getString("telefone"),con.rs.getString("cidade_origem"),con.rs.getString("cidade_destino"),con.rs.getString("data_viagem")});
            }while(con.rs.next());
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Registro não encontrado!");
        }
    TabelaCadastroModel modelo = new TabelaCadastroModel(dados, colunas);
    tableCadastro.setModel(modelo);
    
    tableCadastro.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
    tableCadastro.getColumnModel().getColumn(0).setMaxWidth(40);
    tableCadastro.getColumnModel().getColumn(1).setMaxWidth(350);
    tableCadastro.getColumnModel().getColumn(2).setMaxWidth(120);
    tableCadastro.getColumnModel().getColumn(3).setMaxWidth(160);
    tableCadastro.getColumnModel().getColumn(4).setMaxWidth(160);
    tableCadastro.getColumnModel().getColumn(5).setMaxWidth(100);
    
    tableCadastro.getTableHeader().setReorderingAllowed(true);
    tableCadastro.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    con.desconectarBanco();
    
    }
    
