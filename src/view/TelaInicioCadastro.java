    private void tableCadastroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCadastroMouseClicked
        
        try {
            
            TabelaCadastroModel modelo = (TabelaCadastroModel)tableCadastro.getModel();
            int seectedRow = tableCadastro.getSelectedRow();
            Date date = new SimpleDateFormat("yyyy-MM-dd").parse((String)modelo.getValueAt(seectedRow, 9).toString());
            jDateChooser1.setDate(date);
         
        if(evt.getClickCount() >1 ){
            
            int row = this.tableCadastro.rowAtPoint(evt.getPoint());
            
            Object obj = (tableCadastro.getValueAt(tableCadastro.getSelectedRow(), 0));
            String id = obj.toString();

            Object obj1 = (tableCadastro.getValueAt(tableCadastro.getSelectedRow(), 1));
            String quant = obj1.toString();
            
            Object obj2 = (tableCadastro.getValueAt(tableCadastro.getSelectedRow(), 2));
            String nome = obj2.toString();

            Object obj3 = (tableCadastro.getValueAt(tableCadastro.getSelectedRow(), 3));
            String telefone = obj3.toString();

            Object obj4 = (tableCadastro.getValueAt(tableCadastro.getSelectedRow(), 4));
            String embarque = obj4.toString();

            Object obj5 = (tableCadastro.getValueAt(tableCadastro.getSelectedRow(), 5));
            String destino = obj5.toString();

            Object obj6 = (tableCadastro.getValueAt(tableCadastro.getSelectedRow(), 6));
            String status = obj6.toString();
            
            Object obj7 = (tableCadastro.getValueAt(tableCadastro.getSelectedRow(), 7));
            String valor = obj7.toString();
            
            Object obj8 = (tableCadastro.getValueAt(tableCadastro.getSelectedRow(), 8));
            String valortotal = obj8.toString();
            
            /*Object obj9 = (tableCadastro.getValueAt(tableCadastro.getSelectedRow(), 9));
            String date = obj9.toString();*/
            
            TelaAterarCadastroView tela1 = new TelaAterarCadastroView();

            tela1.setTextfild1(this, id);
            tela1.setId(this, id);
            tela1.setTextfild2(this, quant);
            tela1.setTextfild3(this, nome);
            tela1.setTextfild4(this, telefone);
            tela1.setTextfild5(this, embarque);
            tela1.setTextfild6(this, destino);
            tela1.setTextfild7(this, status);
            tela1.setTextfild8(this, valor);
            tela1.setTextfild9(this, valortotal);
            tela1.jDateChooser1(this, date);
            

            tela1.setVisible(true);
            dispose();
                    
        }else{
            String id = (String) tableCadastro.getValueAt(tableCadastro.getSelectedRow(), 0);
            con.conectar();
            con.executeSql("SELECT * FROM cadastro WHERE id="+id);
            try {
                con.rs.first();
                mod.setId(con.rs.getInt("id"));
            } catch (SQLException ex) {

            }

        }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "NÃO É POSSIVEL SELECIONAR CAMPO VAZIO, ALTERE O ITEM VAZIO NO BANCO DE DADOS!");
        }
        
    }//GEN-LAST:event_tableCadastroMouseClicked

    private void tableCadastroKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tableCadastroKeyReleased
        
        String id = (String) tableCadastro.getValueAt(tableCadastro.getSelectedRow(), 0);
        System.out.println(id);
        con.conectar();
        con.executeSql("SELECT * FROM cadastro WHERE id="+id);
        try{
            con.rs.first();
            mod.setId(con.rs.getInt("id"));

        } catch(SQLException exception) {
            System.out.println(exception.getMessage());
            JOptionPane.showMessageDialog(null, "Erro ao selecionar");
        }

        con.desconectarBanco();

    }//GEN-LAST:event_tableCadastroKeyReleased

