    public void preencherTabela(String Sql){
        
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"id","quant","nome","telefone","cidade_origem","cidade_destino",
            "status","valorpg","valortotal","data_viagem"};
        con.conectar();
        con.executeSql(Sql);
        try{
            con.rs.first();
            do{
                dados.add(new Object[]{con.rs.getString("id"),con.rs.getString("quant"),con.rs.getString("nome"),
                    con.rs.getString("telefone"),con.rs.getString("cidade_origem"),con.rs.getString("cidade_destino"),
                    con.rs.getString("status"),con.rs.getBigDecimal("valorpg"),con.rs.getBigDecimal("valortotal"),
                    con.rs.getDate("data_viagem")});
            }while(con.rs.next());
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Nenhum registro encontrado!");
            txtPesquisa.setText("");
        }
    TabelaCadastroModel modelo = new TabelaCadastroModel(dados, colunas);
    tableCadastro.setModel(modelo);
    
    DateFormat df = new SimpleDateFormat("dd-MM-yyyy");
    
    tableCadastro.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
    tableCadastro.getColumnModel().getColumn(0).setMaxWidth(44);
    tableCadastro.getColumnModel().getColumn(1).setMaxWidth(44);
    tableCadastro.getColumnModel().getColumn(2).setMaxWidth(550);
    tableCadastro.getColumnModel().getColumn(3).setMaxWidth(150);
    tableCadastro.getColumnModel().getColumn(4).setMaxWidth(240);
    tableCadastro.getColumnModel().getColumn(5).setMaxWidth(240);
    tableCadastro.getColumnModel().getColumn(6).setMaxWidth(120);
    tableCadastro.getColumnModel().getColumn(7).setMaxWidth(100);
    tableCadastro.getColumnModel().getColumn(8).setMaxWidth(100);
    tableCadastro.getColumnModel().getColumn(9).setMaxWidth(120);
    
    
    //tableCadastro.setFont(new Font("Default",Font.PLAIN, 14));
    tableCadastro.getTableHeader().setReorderingAllowed(true);
    tableCadastro.setAutoCreateRowSorter(true); 
    tableCadastro.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    con.desconectarBanco();
    
}
     
    private void Contador() {
        
        int somaTotal=0;
        for (int i=0; i<tableCadastro.getRowCount(); i++)
          somaTotal+=Double.parseDouble(tableCadastro.getValueAt(i, 1).toString());
        txtContador.setText(" "+somaTotal);
        
}
    
